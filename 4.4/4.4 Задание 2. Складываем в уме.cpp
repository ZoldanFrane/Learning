#include <iostream>

//Напишите программу которая проверяет то, как вы умеете складывать два числа в уме.
//Программа должна выводить два разных сообщения на верный и неверный ответ пользователя.
//В последнем случае надо показывать правильный результат.

int main() {
    while (true) { // Бесконечный цикл, чтобы можно было повторять проверку несколько раз подряд
    int a; // Переменная для первого числа
    int b; // Переменная для второго числа
    int sum; // Переменная для суммы, которую введёт пользователь

    std::cout << "Введите первое число: "; // Просим пользователя ввести первое число
    std::cin >> a; // Сохраняем первое число в переменную a

    std::cout << "Введите второе число: "; // Просим пользователя ввести второе число
    std::cin >> b; // Сохраняем второе число в переменную b

    std::cout << "Введите их сумму: "; // Просим пользователя ввести свой ответ (сумму двух чисел)
    std::cin >> sum; // Сохраняем введённую сумму в переменную sum

    std::cout << "-----Проверяем-----" << std::endl; // Разделитель для удобства

    if (sum == a + b) { // Проверяем, правильно ли пользователь сложил числа
        std::cout << "Верно!" << std::endl; // Сообщаем, что ответ пользователя правильный
    } else { // Если сумма неверная
        std::cout << "Ошибка! Верный результат: " << (a + b) << std::endl; // Показываем правильный результат
    }
  }
}
